package com.campusrecruit.bean;

import java.util.Calendar;

public class jobinfo {

	private int _id;
	private String company_name;
	private String interview_address;
	private Calendar interview_date;
	
	public jobinfo(){}
	
	public jobinfo(String name, String addr, Calendar date){
		this.company_name = name;
		this.interview_address = addr;
		this.interview_date = date;
	}
	
	public int get_id() {
		return _id;
	}
	public void set_id(int _id) {
		this._id = _id;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getInterview_address() {
		return interview_address;
	}
	public void setInterview_address(String interview_address) {
		this.interview_address = interview_address;
	}
	public Calendar getInterview_date() {
		return interview_date;
	}
	public void setInterview_date(Calendar interview_data) {
		this.interview_date = interview_data;
	}
	
	
}
